		Condizioni generali Di Contratto
	</b>
</div>
<br><br>
<b>Art. 1 - DEFINIZIONI</b><br>
<div class="testoParagrafo">
<b>1.1</b> Ai sensi ed agli effetti della proposta di compravendita e contestuale prenotazione, nonchè nell’ambito delle presenti condizioni generali di
contratto le Parti danno atto che per brevità deve intendersi come segue: a) Acquirente: il cliente indicato nella proposta di compravendita e
contestuale prenotazione di motoveicolo quale acquirente; b) Venditore: l’impresa titolare di concessione alla vendita di prodotti Honda indicato nella
intestazione della predetta proposta di compravendita; c) Contratto: la proposta di compravendita perfezionata a seguito della sottoscrizione della
stessa da parte del venditore e dell’acquirente; d) Motoveicolo: il Motoveicolo, il Motociclo, il Ciclomotore indicato nella proposta di compravendita e
contestuale prenotazione di motoveicolo sottoscritto dall’acquirente.</div>

<br><br>

<b>Art. 2 - OGGETTO</b><br>
<div class="testoParagrafo">
<b>2.1</b> Il contratto ha ad oggetto la compravendita di un motoveicolo alle condizioni e caratteristiche indicate nella proposta d ’acquisto e contestuale
prenotazione del motoveicolo sottoscritte dall ’acquirente. Le caratteristiche tecniche del motoveicolo saranno quelle risultanti dal certificato di
omologazione del tipo di motoveicolo e garantite dalla relativa dichiarazione di conformità o documento equipollente rilasciati dalla Casa costruttrice .
L’acquirente è reso edotto del fatto che: a) il motoveicolo sarà equipaggiato con pneumatici della marca e del tipo prescelti dalla Casa costruttrice;
b)il motoveicolo potrà essere collaudato e spostato su strada prima della consegna; c) il prezzo convenuto e indicato nella proposta di compravendita
e contestuale prenotazione si intende chiavi in mano.<br>
<b>2.2</b> A fronte della continua evoluzione delle tecniche costruttive del settore motociclistico interessanti sia le parti meccaniche sia le parti di carrozzeria
con relativi elementi decorativi e tonalità cromatiche e delle modifiche necessarie per adeguare il motoveicolo oggetto del contratto alle disposizioni
normative vigenti, l’acquirente prende atto che le caratteristiche del modello, oggetto di compravendita, potrebbero subire modifiche fino alla data di
consegna. Tali modifiche, nei limiti in cui esse siano dirette a migliorare la sicurezza e la qualità del prodotto, derivino da migliorie tecniche, o siano
imposte dalle normative vigenti e comunque non mutino in modo sostanziale l ’oggetto della compravendita, non costituiscono inadempimento del
debitore anche se non comunicate all’acquirente.<br>
<b>2.3</b> Nell’ipotesi in cui l’imposizione di modifiche sostanziali comporti un mutamento di modello, di versione o di motorizzazione, l ’acquirente avrà
facoltà di esercitare il recesso dal presente contratto dandone comunicazione scritta al venditore, mediante lettera a /r, entro sette giorni dalla notizia
dell’intervenuta modifica.

</div>
<br><br>

<b>Art. 3 - PREZZO</b><br>
<div class="testoParagrafo">
<b>3.1</b> Per prezzo chiavi in mano si intende il prezzo di vendita del motoveicolo indicato nella proposta di compravendita, comprensivo di IVA, spese di
trasporto, eventuali accessori richiesti dall ’acquirente, ma al netto dell’imposta addizionale 3.1 Per prezzo chiavi in mano si intende il prezzo di
vendita del motoveicolo indicato nella proposta di compravendita, comprensivo di IVA, spese di trasporto, eventuali accessori richiesti
dall’acquirente, ma al netto dell’imposta addizionale erariale e di trascrizione e di ogni altra spesa o tributo od oneri di legge a cui la compravendita
del veicolo possa essere assoggettata per disposizioni di legge o regolamentari. Sono invece escluse le spese relative al trasferimento di proprietà di
veicoli usati.<br>
<b>3.2</b> In caso di variazioni di aliquote IVA, di diritti e tasse relative all ’immatricolazione e/o alla trascrizione, tale prezzo sarà corrispondentemente
variato.</div>
<br><br>

<b>Art. 4 - CAPARRA</b><br>
<div class="testoParagrafo">
<b>4.1</b> L’acquirente verserà al venditore, all’atto della sottoscrizione della proposta di compravendita ed a titolo di caparra, la somma indicata nell ’ordine
stesso da considerarsi infruttifera e soggetta all’art. 1385 c. c..</div>
<br><br>

<b>Art. 5 - TERMINI DI CONSEGNA DEL MOTOVEICOLO</b><br>
<div class="testoParagrafo">
<b>5.1</b> Non appena il motoveicolo sarà disponibile per la consegna il venditore ne darà tempestiva comunicazione all ’acquirente, indicando gli estremi
identificativi del mezzo. La consegna del motoveicolo si intende fissata entro il termine di cui alla proposta di compravendita e prenotazione
sottoscritta dall’acquirente.<br>
<b>5.2</b> L’acquirente è peraltro a conoscenza che i tempi di consegna sono influenzati da fattori ed eventi indipendenti dal venditore, quali la disponibilit à
di materie prime da parte del costruttore, scioperi, serrate, interruzioni delle catene di montaggio, ostacoli tecnici interessanti stabilimenti di
produzione, indisponibilità di componenti, difficoltà di spedizione o trasporto o da altri fatti al di fuori del controllo del venditore e pertanto il termine di
consegna previsto dal contratto ha carattere solamente orientativo. E’ quindi stabilito un termine di tolleranza di novanta giorni dalla data fissata per
la consegna, solo trascorso il quale l’acquirente avrà diritto di recedere dal contratto.<br>
<b>5.3</b> La comunicazione del recesso dovrà avvenire, nei casi previsti dal presente contratto o dalla legge, in ogni caso a pena di decadenza, prima che
venga effettuata la comunicazione dei dati identificativi del motoveicolo da parte del venditore e dovrà essere fatta per iscritto, mediante lettera a /r.
La comunicazione del recesso in detta ipotesi comporterà per il venditore l ’obbligo di restituire entro 20 giorni la somma ricevuta a titolo di caparra,
infruttifera di interessi.
</div>
<br><br>

<b>Art. 6 - PAGAMENTO DEL PREZZO</b><br>
<div class="testoParagrafo">
<b>6.1</b> Il saldo del prezzo del motoveicolo deve avvenire prima dell ’immatricolazione dello stesso o della sua presa in consegna da parte dell ’acquirente
ed in ogni caso entro dieci giorni dalla comunicazione scritta dei dati identificativi del medesimo da parte del venditore .<br>
<b>6.2</b> Il saldo dovrà essere effettuato presso la sede del venditore, che rilascerà all’acquirente regolare documento fiscale.</div>
<br><br>

<b>Art. 7 - OBBLIGHI DELL’ACQUIRENTE</b><br>
<div class="testoParagrafo">
<b>7.1</b> Qualora l’acquirente non provveda integralmente al pagamento del saldo nei termini previsti, o non provveda alla presa in consegna del
motoveicolo entro dieci giorni dalla data in cui sia stato comunicato per iscritto che il motoveicolo è a sua disposizione, il venditore potrà considerare
risolto di diritto il presente contratto per inadempimento dell’acquirente e trattenere, a titolo di risarcimento del danno, la caparra ricevuta.</div>
<br><br><br>

<b>Art. 8 - GARANZIA</b><br>
<div class="testoParagrafo">
<b>8.1</b> Il motoveicolo nuovo e di prima immatricolazione è coperto dalle garanzie del Produttore e del Venditore applicabili secondo vigenti leggi, garanzia
vincolata all’esecuzione dei regolari tagliandi manutentivi in centri autorizzati e previa registrazione ed esibizione del Certificato di Garanzia e libretto
di uso e manutenzione; l’acquirente dichiara di aver letto e confermare i propri obblighi di regolare manutenzione programmata ed uso del motoveicolo
e di rispettare le vincolanti condizioni per la permanenza d’efficacia delle garanzie.</div>
<br><br>

<b>Art. 9 - DIRITTO DI RECESSO</b><br>
<div class="testoParagrafo">
<b>9.1</b> Ove il presente contratto sia stato concluso a distanza ovvero negoziato al di fuori dei locali commerciali del venditore ed il cliente rivesta la qualità
di consumatore, quest’ultimo ha diritto di recedere dal presente contratto inviando al venditore una comunicazione per iscritto nei modi e termini
previsti degli artt. 64 e 65 del Codice del Consumo (D. Lgs. 206/2005).</div>
<br><br>

<b>Art. 10 - MOTOVEICOLO USATO</b><br>
<div class="testoParagrafo">
<b>10.1</b> Qualora l’acquirente abbia previsto di corrispondere parte del prezzo d ’acquisto del motoveicolo cedendo in permuta altro motoveicolo usato di
sua proprietà o di proprietà di terzi di cui l ’acquirente abbia la disponibilità materiale e giuridica, il valore di permuta - che verrà imputato a parziale
pagamento del prezzo del motoveicolo oggetto dell ’ordine d’acquisto - viene fissato nel rapporto di stima del mezzo ceduto in permuta, che forma
parte integrante ed inscindibile del presente atto di compravendita. Il rapporto di stima, descrive altresì lo stato di conservazione ed uso del mezzo
oggetto di permuta e viene redatto ed accettato da entrambe le parti. L’acquirente che cede in permuta un motoveicolo usato, garantisce che lo
stesso non abbia vizi ulteriori rispetto a quelli indicati nel rapporto di stima derivato dalle di lui affermazioni e vista del mezzo usato; egli garantisce
altresì la piena disponibilità giuridica del mezzo, nonchè l’assenza di vincoli, privilegi, o di altri diritti di terzi sullo stesso; egli garantisce inoltre la
veridicità del chilometraggio risultante dalla strumentazione del motoveicolo, così come riportata nel rapporto di stima.</div>
<br><br>

<b>Art .11 - PATTI AGGIUNTI</b><br>
<div class="testoParagrafo">
<b>11.1</b> 11.1 Eventuali aggiunte in deroga a quanto previsto dal presente contratto ovvero aggiunte o deroghe nella proposta d ’acquisto e contestuale
ordinazione saranno vincolanti solo se debitamente pattuite per iscritto.<br>
<b>11.2</b> 11.2 L’acquirente prende atto che il venditore è autonomo imprenditore e non ha alcun potere di rappresentanza nè può in alcun modo assumere
obbligazioni in nome e per conto del costruttore del motoveicolo o della Honda Motor Europe Ltd. - Italia.
</div>
<br><br>

<b>Art. 12 - NORMA INTEGRATIVA</b><br>
<div class="testoParagrafo">
<b>12.1</b> Per ogni e qualsiasi aspetto non espressamente contemplato nelle presenti disposizioni generali restano applicabili le vigenti leggi civili in materia
di compravendita, nonchè le disposizioni di legge in materia di circolazione stradale.</div>
<br><br>

<b>Art. 13 - FORO COMPETENTE</b><br>
<div class="testoParagrafo">
<b>13.1</b> Per ogni controversia che dovesse sorgere tra acquirente e venditore inerente alla validit à, all’ interpretazione, all’esecuzione o alla risoluzione
del presente contratto, foro territorialmente competente, esclusivo ed inderogabile sarà quello del luogo in cui ha sede il venditore.</div>
