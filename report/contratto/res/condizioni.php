nonchè per ogni altro effetto di legge. In particolare l ’acquirente dichiara di aver preso visione e di accettare le seguenti clausole: 4.1 caparra; 5.2
termini consegna; 5.3 recesso; 7.1 risoluzione per mancato ritiro da parte acquirente; 8.1 garanzia venditore e garanzia produttore, obblighi acquirente
ad esecuzione tagliandi ed altri, pena automatica decadenza dalle garanzie; 11.2 assoluta distinzione e diversità tra venditore e produttore; 13.1. Foro
